// ===== Utility Helpers =====

// Generate a unique ID
export function generateId(prefix = '') {
  return prefix + Date.now().toString(36) + Math.random().toString(36).slice(2, 7);
}

// Format date to readable string
export function formatDate(dateStr) {
  const d = new Date(dateStr);
  return d.toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric' });
}

// Format date and time
export function formatDateTime(dateStr) {
  const d = new Date(dateStr);
  return d.toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric', hour: '2-digit', minute: '2-digit' });
}

// Format time only
export function formatTime(dateStr) {
  const d = new Date(dateStr);
  return d.toLocaleTimeString('en-US', { hour: '2-digit', minute: '2-digit' });
}

// Time ago (e.g., "5 min ago")
export function timeAgo(dateStr) {
  const diff = Date.now() - new Date(dateStr).getTime();
  const mins = Math.floor(diff / 60000);
  if (mins < 1) return 'Just now';
  if (mins < 60) return `${mins} min ago`;
  const hours = Math.floor(mins / 60);
  if (hours < 24) return `${hours}h ago`;
  const days = Math.floor(hours / 24);
  return `${days}d ago`;
}

// Get day and month from date
export function getDayMonth(dateStr) {
  const d = new Date(dateStr);
  return {
    day: d.getDate(),
    month: d.toLocaleDateString('en-US', { month: 'short' })
  };
}

// Escape HTML to prevent XSS
export function escapeHtml(str) {
  const div = document.createElement('div');
  div.textContent = str;
  return div.innerHTML;
}

// Get initials from a name
export function getInitials(name) {
  return name.split(' ').map(n => n[0]).join('').toUpperCase().slice(0, 2);
}

// Debounce function
export function debounce(fn, delay = 300) {
  let timer;
  return (...args) => {
    clearTimeout(timer);
    timer = setTimeout(() => fn(...args), delay);
  };
}

// Generate available time slots for a date
export function getTimeSlots(date) {
  const slots = [];
  const now = new Date();
  const selectedDate = new Date(date);
  const isToday = selectedDate.toDateString() === now.toDateString();

  for (let hour = 8; hour < 18; hour++) {
    for (let min = 0; min < 60; min += 30) {
      if (isToday && (hour < now.getHours() || (hour === now.getHours() && min <= now.getMinutes()))) {
        continue;
      }
      const time = `${hour.toString().padStart(2, '0')}:${min.toString().padStart(2, '0')}`;
      slots.push({ time, available: Math.random() > 0.3 }); // Simulate availability
    }
  }
  return slots;
}

// Get today's date string (YYYY-MM-DD)
export function getTodayString() {
  return new Date().toISOString().split('T')[0];
}

// Priority category label and styling
export function getPriorityDisplay(category) {
  const map = {
    'EMERGENCY': { label: 'Emergency', badge: 'badge-emergency', dot: 'high', color: 'var(--color-danger-500)' },
    'PRIORITY': { label: 'Urgent', badge: 'badge-urgent', dot: 'medium', color: 'var(--color-warning-500)' },
    'NON_URGENT': { label: 'Normal', badge: 'badge-normal', dot: 'low', color: 'var(--color-success-500)' }
  };
  return map[category] || map['NON_URGENT'];
}

